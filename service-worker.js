// service-worker.js
const CACHE_NAME = 'phase-cache-v1';
const urlsToCache = [
  './',
  './index.html'
];

// التثبيت وتخزين الملفات في الكاش
self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        console.log('Opened cache');
        return cache.addAll(urlsToCache);
      })
  );
});

// تفعيل وحذف الكاش القديم
self.addEventListener('activate', event => {
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (cacheName !== CACHE_NAME) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});

// استراتيجية التحميل: من الكاش أولاً، ثم من الشبكة
self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        // موجود في الكاش -> أعطه
        if (response) {
          return response;
        }
        // غير موجود -> حمله من الشبكة
        return fetch(event.request).then(
          response => {
            // تأكد من أن الرد صالح
            if(!response || response.status !== 200 || response.type !== 'basic') {
              return response;
            }
            // خزن النسخة في الكاش للمرة القادمة
            const responseToCache = response.clone();
            caches.open(CACHE_NAME)
              .then(cache => {
                cache.put(event.request, responseToCache);
              });
            return response;
          }
        );
      })
    );
});